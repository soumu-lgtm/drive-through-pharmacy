  // 名前推測メイン関数
  function guessKanji(kataReading) {
    if (!kataReading) return null;
    const normalized = kataReading.replace(/[\s　]/g, '');

    // スペースやミドルドットで姓名分割を試みる
    const origParts = kataReading.trim().split(/[\s　・]+/);
    if (origParts.length >= 2) {
      const surnameCandidates = SURNAME[origParts[0]] || [];
      const givenCandidates = MALE_GIVEN[origParts[1]] || FEMALE_GIVEN[origParts[1]] || [];
      if (surnameCandidates.length > 0 || givenCandidates.length > 0) {
        return {
          surname: surnameCandidates[0] || null,
          given: givenCandidates[0] || null,
          surnameCandidates,
          givenCandidates: [...(MALE_GIVEN[origParts[1]] || []), ...(FEMALE_GIVEN[origParts[1]] || [])],
          full: (surnameCandidates[0] || origParts[0]) + ' ' + (givenCandidates[0] || origParts[1]),
          confidence: (surnameCandidates.length > 0 ? 0.5 : 0) + (givenCandidates.length > 0 ? 0.5 : 0)
        };
      }
    }
    return { surname: null, given: null, surnameCandidates: [], givenCandidates: [], full: null, confidence: 0 };
  }

  // 性別推測（名前から）
  function guessSex(givenKata) {
    if (!givenKata) return null;
    if (MALE_GIVEN[givenKata]) return '男';
    if (FEMALE_GIVEN[givenKata]) return '女';
    // 末尾ヒューリスティック
    if (/[コ]$/.test(givenKata)) return '女';
    if (/[ロウスケタ]$/.test(givenKata)) return '男';
    if (/[ミカナリエイ]$/.test(givenKata)) return '女';
    return null;
  }

  return { guessKanji, guessSex, SURNAME, MALE_GIVEN, FEMALE_GIVEN };
