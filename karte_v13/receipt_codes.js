// レセプト種別コード解読
// 4桁: [0]医科1/歯科3 [1]保険種別 [2]本人家族 [3]入外+年齢区分
function parseInsuranceType(code) {
  if (!code || code.length < 4) return code || '不明';
  const d1 = code[1]; // 保険種別
  const d2 = code[2]; // 本人/家族
  const d3 = code[3]; // 入外+年齢区分

  let insurance = '';
  switch (d1) {
    case '1': insurance = '社保'; break;
    case '2': insurance = '公費'; break;
    case '3': insurance = '国保'; break;
    case '4': insurance = '退職'; break;
    case '6': insurance = '後期高齢'; break;
    default: insurance = '保険(' + d1 + ')';
  }

  // 本人/家族
  if (d2 === '1') insurance += '本人';
  else if (d2 === '2') insurance += '未就学';
  else if (d2 === '3') insurance += '家族'; // 国保世帯主以外

  // 年齢区分
  switch (d3) {
    case '0': break; // 一般
    case '2': insurance += '(本人外来)'; break;
    case '4': insurance += '(6歳未満)'; break;
    case '6': insurance += '(家族外来)'; break;
    case '8': insurance += '(高齢)'; break;
    // default: 何もしない
  }
  return insurance;
}
